package ex0627;

public class Ex02 {
	public static void main(String[] args) {
		
		//ctrl shift f 자동정렬 = 괄호갯수 안맞으면 작동안됨
		int num = 5;
		
		System.out.print(num++);
		System.out.print(num);
		System.out.println();
		System.out.println(num--);
		System.out.println(num);
		
		System.out.println(++num);
	}
}